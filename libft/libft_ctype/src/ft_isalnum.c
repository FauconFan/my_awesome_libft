#include "libft_ctype.h"

t_bool		ft_isalnum(int c)
{
	return (((c >= '0' && c <= '9') ||
		(c >= 'a' && c <= 'z') ||
		(c >= 'A' && c <= 'Z')) ? TRUE : FALSE);
}
