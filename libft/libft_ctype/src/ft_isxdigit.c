#include "libft_ctype.h"

t_bool		ft_isxdigit(int c)
{
	return (ft_isdigit(c) ||
		(c >= 'a' && c <= 'f') ||
		(c >= 'A' && c <= 'F') ? TRUE : FALSE);
}
