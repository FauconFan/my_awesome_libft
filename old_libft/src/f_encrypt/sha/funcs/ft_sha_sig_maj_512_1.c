#include "libft.h"

uint64_t	ft_sha_sig_maj_512_1(uint64_t x)
{
	return ((ft_rotright_uint64(x, 14))
			^ (ft_rotright_uint64(x, 18))
			^ (ft_rotright_uint64(x, 41)));
}
