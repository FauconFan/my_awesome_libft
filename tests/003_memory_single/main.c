#include "libft_memory.h"

int		main(void)
{
	char	*d;

	d = ft_memalloc(512);
	for (size_t i = 0; i < 512; ++i)
		d[i] = i % 256;
	ft_bzero(d + 128, 64);
	ft_memprint(d, 512);
	free(d);
}
